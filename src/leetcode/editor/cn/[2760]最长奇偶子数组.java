class Solution {
    public int longestAlternatingSubarray(int[] nums, int threshold) {
        int[] dp = new int[nums.length];
        for (int i = 0; i < nums.length; i++) {
            if (nums[i] > threshold) {
                dp[i] = -1;
            } else {
                dp[i] = nums[i] % 2;
            }
        }
        int result = 0, t = 0;
        for (int i = 0; i < dp.length; i++) {
            if (dp[i] == (t % 2) && dp[i] != -1) {
                t++;
            } else {
                result = Math.max(t, result);
                if (dp[i] == 0) {
                    t = 1;
                } else {
                    t = 0;
                }
            }
        }
        result = Math.max(t, result);
        return result;

    }
}
