class Solution {
    public int countPrimeSetBits(int left, int right) {
        int sum = 0;
        for (int i = left; i <= right; i++) {
            int num = num(i);
            if (zhishu(num)) {
                //   System.out.println(num);
                sum++;
            }
        }
        return sum;
    }

    int num(int n) {
        int i = 0;
        while (n > 0) {
            if ((n & 1) == 1) {
                i++;
            }
            n >>= 1;
        }
        return i;
    }

    boolean zhishu(int i) {
        if (i == 1) {
            return false;
        }
        for (int l = 2; l < i / 2 + 1; l++) {
            if (i % l == 0) {
                return false;
            }
        }
        return true;
    }
}
