import java.util.HashSet;

/**
 * Definition for singly-linked list.
 * class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) {
 *         val = x;
 *         next = null;
 *     }
 * }
 */
public class Solution {
    public ListNode detectCycle(ListNode head) {
        ListNode temp = head;
        HashSet<ListNode> hs = new HashSet<>();
        while (temp != null && !hs.contains(temp)) {
            hs.add(temp);
            temp = temp.next;
            if (temp == null) {
                return null;
            }
        }
        return temp;

    }
}
