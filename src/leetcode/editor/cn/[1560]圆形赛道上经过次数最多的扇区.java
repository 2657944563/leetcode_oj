class Solution {
    public List<Integer> mostVisited(int n, int[] rounds) {
        int l = rounds[0],
                r = rounds[rounds.length - 1];
        ArrayList<Integer> list = new ArrayList<>();
        while (true) {
            list.add(l);
            if (l == r) {
                break;
            } else if (l == n) {
                l = 0;
            }
            l = l + 1;

        }
        Collections.sort(list);
        return list;
    }

}
